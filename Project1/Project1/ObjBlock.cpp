//ドロー
void CObjBlock::Draw()
{
	//描画カラー情報
	float c[4] = { 1.0f,1.0f,1.0f,1.0f };

	RECT_F src;//描画元切り取り位置
	RECT_F dst;//描画先表示位置

	//背景表示
	src.m_top = 256.0f;
	src.m_left = 0.0f;
	src.m_right = 512.0f;
	src.m_bottom = 512.0f;
	src.m_top = 0.0f;
	src.m_left = 0.0f;
	src.m_right = 800.0f;
	src.m_bottom = 600.0f;
	Draw::Draw(0, &src, &dst, c, 0.0f);

	//マップチップによるblock設置
	//切り取り位置の設定
	src.m_top = 0.0f;   //y
	src.m_left = 320.0f; //x
	src.m_right = src.m_left+64.0f; //x
	src.m_bottom = 64.0f; //y

	for (int i = 0; i < 28; i++)
	{
		for (int j = 0; j < 55; j++)
		{
			if (m_map[i][j] > 0)
			{
				//表示位置の設定
				dst.m_top = 0.0f;
				dst.m_left = 0.0f;
				dst.m_right = 64.0;
				dst.m_bottom = 64.0;

				//描画
				Draw::Draw(7, &src, &dst, c, 0.0f);
			}
		}
	}
}